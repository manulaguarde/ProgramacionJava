package juegoTresEnRaya;

public class OtroTablero {

	public static void main(String[] args) {
		
		System.out.println("-------------");
		for(int filas=0;filas<3;filas++) {
			for(int columnas=0;columnas<3;columnas++) {
				System.out.print("| "+" "+" ");
			}
			System.out.println("|\n-------------");
			
		}

	}

}
